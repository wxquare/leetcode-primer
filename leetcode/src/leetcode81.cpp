class Solution {
public:
    bool search(vector<int>& nums, int target) {
        int n = nums.size();
        int left = 0;
        int right = n -1;

        while(left <= right){
            int mid = (left + right)/2;
            if(target == nums[mid]) return true;

            //right is sorted
            if(nums[mid] < nums[right]){
                if(target > nums[mid] && target<=nums[right]){
                    left = mid+1;
                }else{
                    right = mid - 1;
                }
            }else if(nums[mid] > nums[right]){ //left is right
                if(target>=nums[left] && target<nums[mid]){
                    right = mid - 1;
                }else{
                    left = mid + 1;
                }

            }else{
                int tmp = right;
                while(nums[mid] == nums[tmp] && mid != tmp){
                    tmp--;
                }
                if(nums[mid] < nums[tmp]){
                    if(target > nums[mid] && target<=nums[tmp]){
                        left = mid+1;
                        right = tmp;
                    }else{
                        right = mid - 1;
                    }
                }else if(nums[mid] > nums[tmp]){
                    if(target >= nums[left] && target<nums[mid]){
                        right = mid - 1;
                    }else{
                        left = mid + 1;
                        right = tmp;
                    }
                }else{
                    right = mid - 1;
                }
            }
        }
        return false;
    }
};

